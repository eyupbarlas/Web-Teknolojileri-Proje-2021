            <h1>Harput Kalesi</h1>
            <p>Elazığ ilinde bulunan ve adını bulunduğu Harput ilçesinde alan Harput Kalesi'nin tarihi oldukça eskilere dayanmaktadır. Süt Kalesi olarak de bilinen Harput Kalesi'nin bu şekilde isimlendirilmesinin sebebi olarak bir rivayet anlatılır. Rivayete göre kalenin inşası esnasında harca su değil de süt karıştırılmış olmasıdır. Süt karıştırılmasının sebebi olarak da kalenin inşa edildiği yıllarda bölgede su kıtlığı yaşandığı için süt kullanıldığı söylene gelmiştir. Harcına süt karıştırıldığı düşünüldüğü için de halk arasında Harput Kalesi'ne Süt Kalesi de denilmektedir.</p>
            <p>Kalede yapılan akademik araştırmalar sonucu bu yapının Urartu'lar tarafından M.Ö. 8 yy. yapılmış olduğuna dair bulgulara erişilmiştir. O dönemlerden günümü kadar gelmeyi başaran nadir yapılar arasındaki Harput Kalesi, zamanında bölgeyi ele geçiren beylikler ve Osmanlı döneminde onarımlar görmüştür. Son olarak da cumhuriyet döneminde restorasyon görmüştür.</p>
            <p>Dikdörtgen bir yapıya sahip olan Harput Kalesi iki bölümden oluşmaktadır. Bu iki bölümden biri iç kale, diğeri de dış surlardır. Ancak bu dış surların büyük bölümü gönümüze kadar gelmeyi başaramamıştır. Kalenin kullanıldığı dönemlerde içinde; hastane, cephanelik, tahıl ambarı ve sarnıç gibi bölümleri mevcutmuş. Kalenin dış surlarının çevrelediği yer ise birçok konutu ve dükkanı içine alan bir yerleşim yeri durumundaymış.</p>

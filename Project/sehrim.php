                <h1>Gakgoşlar Diyarı Elazığ</h1>
                <p>
                    Elâzığ, Türkiye'nin Elâzığ ilinin merkezi olan şehir. Ülkenin en kalabalık 37. şehri olan Elâzığ'ın merkezi, 2020 itibarıyla 589.267 kişilik merkez nüfusuna sahiptir. Doğu Anadolu Bölgesi'nin Yukarı Fırat Bölümü'nde yer almaktadır.
                    Elazığ, tarih boyunca önemli yerleşim alanlarından biri olmuştur. Elazığ’ın geçmişi yerleşim yeri olarak 19. yüzyıla dayansa da kent sınırları içerisinde yer alan Harput'un tarihi M.Ö. 2000’li yıllara kadar uzanır. Elazığ’ın mâden bakımından zengin ve Türkiye’nin en büyük hidroelektrik santralının bu ilde oluşu ile sanayi gelişmiştir.
                </p>
                <h2>Gezilebilecek Yerler</h2>
                <p>
                    Kentte bulunan tarihi eserler, abideler ve kalıntıların çoğu Harput sınırları içerisindedir. Tarih boyunca önemli yerleşim alanlarından biri olan Harput'u gezerken tarihe doğru bir yolculuğa çıkarsınız. Kentleşme felsefesinin yaşamış olduğu dönüşüm Harput'un bugünkü Elazığ'ı merkez almasına neden olmuştur.
                </p>
                <p>
                    Harput Kalesi Urartular tarafından inşa edilmiştir. Bu görkemli yapı İç ve Dış Kale'den meydana gelmektedir.
                </p>
                <p>
                    Harput Kalesi'nin hemen solunda bulunan Meryem Ana Kilisesi, 6. yüzyılda Süryaniler tarafından inşa edilmiş en eski kiliselerden biridir. Süryani Kilisesi, Kızıl Kilise veya Yakubi Kilisesi olarak da adlandırılan Meryem Ana Kilisesi kaleye çıkan gizli yollara sahiptir.
                </p>
                <p>
                    Harput'un en eski camisi olan Ulu Camii M.S. 1156-1157 yıllarında inşa edilmiştir. Sarahatun, Alacalı, Kurşunlu ve Ağa Camileri kentteki diğer başlıca tarihi önemi büyük dini yapılardır.
                </p>
                <p>
                    Elazığ Arkeoloji ve Etnoğrafya Müzesi Fırat Üniversitesi Kampüsü içerisinde yer alır. Müze, Türkiye'nin en önemli müzelerinden biri olarak tarih tutkunlarına hizmet verir.
                </p>
                <p>
                    Hazar Gölü Elazığ'ın doğal güzelliğinin en güzel yüzüdür. Kentin güneydoğusunda kent merkezinden 25 km uzaklıktaki göl, 86 km²'lik alana yayılmış 22 km uzunluğunda tektonik bir göldür.
                </p>
                <p>
                    Ayrıca Türkiye'nin en büyük baraj göllerinden biri olan Keban ve Buzluk Mağarası, Elazığ'ın gezmeniz gereken diğer güzelliklerindendir.
                </p>
                <p>
                    Harput sınırları içerisinde yer alan Arapbaba Türbesi de tarihi öneme sahiptir.
                </p>
